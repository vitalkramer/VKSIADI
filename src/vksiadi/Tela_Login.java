    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Processo_Login login = new Processo_Login();
        Funcoes md5 = new Funcoes();
        int valor = 0;
        
        login.setUsu_usuario(campoUsuario.getText());
        login.setUsu_senha(md5.md5(campoSenha.getText()));
        
        if(campoUsuario.getText().equals("") || campoSenha.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campos obrigatórios não preenchidos.");
        }else{
            try {
                valor  = login.logar();
                if(valor == 1){
                    dispose();
                    Tela_Principal telaPrincipal = new Tela_Principal();
                    telaPrincipal.setVisible(true);
                }
                if(valor == 0){
                    JOptionPane.showMessageDialog(null, "Usuário ou Senha Incorretos.");
                    this.campoUsuario.setText("");
                    this.campoSenha.setText("");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(Tela_Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

